class Conversion
{
public static void main(String args[])
{
byte b=19;
char ch='c';
short s=20;
int i=28;
float f=20.05F;
double d=345.34;
long l=23;
boolean bo=true;
Byte byteobj=b;
Character charobj=ch;
Short sobj=s;
Integer intobj=i;
Float floatobj=f;
Double doubleobj=d;
Long longobj=l;
Boolean boolobj=bo;
System.out.println("printing obj values");
System.out.println("byte code:"+byteobj);
System.out.println("charcode:"+charobj);
System.out.println("Shortcode:"+sobj);
System.out.println("intcode:"+intobj);
System.out.println("floatcode:"+floatobj);
System.out.println("doubleobj:"+doubleobj);
System.out.println("longobj:"+longobj);
System.out.println("booleanobj:"+boolobj);
byte bytevalue=byteobj;
char charvalue=charobj;
short shortvalue=sobj;
int intvalue=intobj;
float floatvalue=floatobj;
double doublevalue=doubleobj;
long longvalue=longobj;
boolean boolvalue=boolobj;
System.out.println("printing primitive values");
System.out.println("byte value:"+bytevalue);
System.out.println("char value:"+charvalue);
System.out.println("Shortvalue:"+shortvalue);
System.out.println("intvalue:"+intvalue);
System.out.println("floatvalue:"+floatvalue);
System.out.println("doublevalue:"+doublevalue);
System.out.println("longvalue:"+longvalue);
System.out.println("booleanvalue:"+boolvalue);
}
}
